import streamlit as st



# ==========================================================
# INITIALISATION SESSION ASTRO IA
# ==========================================================


def init_session(config):
    """
    Initialise toutes les variables de session
    utilisées par Astro IA.
    """



    defaults = {



        # ==================================================
        # CONFIGURATION
        # ==================================================

        "config": config,

        "config_validated": False,



        # ==================================================
        # WORKFLOW
        # ==================================================

        "analysis_ready": False,

        "fits_loaded": False,

        "simbad_ready": False,

        "report_ready": False,



        # ==================================================
        # FICHIER IMAGE
        # ==================================================

        "image_path": "",

        "image_name": "",

        "fits_path": "",



        # ==================================================
        # HEADER FITS
        # ==================================================

        "fits_header": {},



        # ==================================================
        # OBJET ASTRO
        # ==================================================

        "object_name": "",

        "object_type": "",

        "instrument": "",

        "camera": "",



        # ==================================================
        # COORDONNEES
        # ==================================================

        "ra": None,

        "dec": None,



        # ==================================================
        # ACQUISITION
        # ==================================================

        "focal_length": None,

        "exposure": None,

        "gain": None,

        "ccd_temp": None,



        # ==================================================
        # SIMBAD
        # ==================================================

        "simbad_data": {},



        # ==================================================
        # OPTIQUE
        # ==================================================

        "fov_data": {},

        "optical_analysis": {},



        # ==================================================
        # IA
        # ==================================================

        "analysis_result": "",



        # ==================================================
        # WORKFLOW TRAITEMENT
        # ==================================================

        "siril_workflow": "",

        "gimp_workflow": "",



        # ==================================================
        # RAPPORT
        # ==================================================

        "report_text": "",

        "report_file": ""

    }



    # ======================================================
    # CREATION VARIABLES
    # ======================================================


    for key, value in defaults.items():


        if key not in st.session_state:

            st.session_state[key] = value